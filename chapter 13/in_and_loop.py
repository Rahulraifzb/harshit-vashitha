# in keyword in sets and for loop

s = {"a","b","c"}

# in keyword to check if item is present or not in set
if "a" in s:
    print("Present")
else:
    print("not present")


# for loop
for item in s:
    print(item)


# Operation

s1 = {1,2,3,4}
s2 = {3,4,5,6}

# Union
# intersection

print(s1 | s2) # union
print(s1 & s2) # Intersection